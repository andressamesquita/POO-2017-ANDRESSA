package quest02TEORICA;

public class ConceitoAtributosMetodos {

	/*
	 * Os ATRIBUTOS são caracteristicas relacionadas aos substantivos (objetos). 
	 * Definem, ainda, o estado dos objetos.
	 * EXEMPLO ----> Blog: postagem, nome, URL
	 *
	 * Os METODOS são os verbos que representam comportamentos e ações que os objetos podem realizar.
	 * São implementados através de um conjunto de instruções, semelhanate às funções. 
	 * EXEMPLO ----> Blog: exibir postagens, excluir comentarios, pesquisar.
	 */
}
